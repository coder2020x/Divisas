    private void EscalarImagen(JLabel labelNmae , String root){
        ImageIcon image = new ImageIcon(root);
        Icon icon = new  ImageIcon(image.getImage().getScaledInstance(labelNmae.getWidth(),labelNmae.getHeight(),Image.SCALE_DEFAULT));
        labelNmae.setIcon(icon);
        this.repaint();
    }
