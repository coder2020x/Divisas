    private void ComboListen(){
            metodo.putClientProperty( "JComponent.roundRect", true );
            UIManager.put( "ScrollBar.thumbArc", 3 );
            UIManager.put( "ScrollBar.thumbInsets", new Insets( 2, 2, 2, 2 ) );
            metodo.addItemListener((ItemEvent event) -> {
            String item = (String) event.getItem();
            //System.out.println("Affected items: " + item + "\n");
            if (event.getStateChange() == ItemEvent.SELECTED) {
                if("Conversor Moneda".equals(item)){
                    System.out.println(item+" Correcto");
                    cargaConversor(new Fconversor());
                }
                if("Conversor Temperatura".equals(item)){
                    cargaTemperatura(new FormTemperatura());
                }
            }
            if (event.getStateChange() == ItemEvent.DESELECTED){
               // System.out.println(item + " deselected\n");
            }
          });
    }
    private void cargaConversor(Fconversor p) {
        p.setLocation(0,0); 
        Dimension tamano = Form.getPreferredSize();
        p.setSize(tamano);
        Form.removeAll();
        Form.add(p,BorderLayout.CENTER);
        Form.setVisible(true);
        Form.revalidate();
        Form.repaint(); 
    }
